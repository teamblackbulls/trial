package staff.model;

public class Prayer {
	private int id;
	private String prayername;
	

	
	public Prayer() {
		
	}

	public int getId() {
		return id;
	}


	public void setId(int id) {
		this.id = id;
	}


	public String getPrayername() {
		return prayername;
	}

	public void setPrayername(String prayername) {
		this.prayername = prayername;
	}

	
	
	
}
